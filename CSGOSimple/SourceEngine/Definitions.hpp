	template <typename FuncType>
	FuncType CallVFunction(void* ppClass, size_t index)
	{
		const unsigned long* pVTable = *static_cast<const unsigned long**>(ppClass);
		DWORD dwAddress = pVTable[index];
		return (FuncType)(dwAddress);
	}

